#include <iostream>
using namespace std;

int main(){
	int x = 5;
	int y =2;
	cout << x + y << endl;   //SUMA SUMA DOS VALORES X + Y
	cout << x - y << endl;   //RESTA RESTA UN VALOR DE OTRO X - Y
	cout << x * y << endl;   //MULTIPLICACIÓN MULTIPLICA DOS VALORES X * Y
	
	x = 12;
	y = 3;
	cout << x/y << endl;    //Divide un valor por otro x / y
	
	x = 5;
	y = 2;
	cout << x % y << endl;   //Devuelve el residuo de la división x% y
	
	++x;
	cout << x  << endl;
	
	x = 5;
	--x;                  // Disminuye el valor de una variable en 1
	cout << x << endl;
	
	return 0;
		
	
	
}
