#include <iostream>
using namespace std;

int main() {
	
	int x = 5;
	cout << x << endl;
	
	x += 3;                  // x = x + 3   + : Suma
	cout << x << endl;
	
    x -= 3;                  // x = x - 3   - : Resta
	cout << x << endl;
	
	x *= 3;                  // x = x * 3   * : Multiplicación
	cout << x << endl;
	
	x /= 3;                  // x = x / 3   / : División
	cout << x << endl;
	
	x %= 3;                  // x = x % 3   % : Modulo o residuo
	cout << x << endl;
	
	return 0;
}
